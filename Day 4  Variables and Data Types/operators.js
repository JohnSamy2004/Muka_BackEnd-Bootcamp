//Arithmetic Operators
console.log(5 + 3);  // Addition: 8
console.log(5 - 3);  // Subtraction: 2
console.log(5 * 3);  // Multiplication: 15
console.log(5 / 3);  // Division: 1.666...
console.log(5 % 3);  // Modulus: 2
console.log(5 ** 3); // Exponentiation: 125

//Comparison Operators
console.log(5 > 3);   // Greater than: true
console.log(5 >= 5);  // Greater than or equal: true
console.log(5 < 3);   // Less than: false
console.log(5 <= 3);  // Less than or equal: false
console.log(5 == '5'); // Loose equality (type coercion): true
console.log(5 === '5'); // Strict equality (no coercion): false
console.log(5 != '5'); // Loose inequality: false
console.log(5 !== '5'); // Strict inequality: true

//Logical Operators
console.log(true && false);  // AND: false
console.log(true || false);  // OR: true
console.log(!true);          // NOT: false