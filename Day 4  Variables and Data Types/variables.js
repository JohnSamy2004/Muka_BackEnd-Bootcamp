// Using var
var x = 10;
console.log("var x:", x); // var allows redeclaration
x = 20;
console.log("Updated var x:", x);

// Using let
let y = 30;
console.log("let y:", y); // let does not allow redeclaration
y = 40;
console.log("Updated let y:", y);

// Using const
const z = 50;
console.log("const z:", z); // const does not allow reassignment or redeclaration
// z = 60; // Uncommenting this line will throw an error
